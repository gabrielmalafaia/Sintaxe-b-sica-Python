# Operadores utlizados para definir o valor inicial ou sobrescrever o valor de uma variável

saldo = 500 # atribuição simples
saldo_ad = 500
saldo_ad += 200 # atribuição com adição
saldo_sub = 700
saldo_sub -= 100 # atribuição com subtração
saldo_mult = 500
saldo_mult *= 2 # atribução com multiplicação
saldo_div = 500
saldo_div /= 5 # atribuição com divisão
saldo_div_int = 500
saldo_div_int //= 5 # atribuição com divisão inteiro
saldo_mod = 500
saldo_mod %= 480 # atribuição com módulo
saldo_exp = 500
saldo_exp **= 2 # atribuição com exponenciação

print(saldo)
print(saldo_ad)
print(saldo_sub)
print(saldo_mult)
print(saldo_div)
print(saldo_div_int)
print(saldo_mod)
print(saldo_exp)


