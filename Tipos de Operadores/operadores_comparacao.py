# Operadores utilizados para comparar dois valores

saldo = 450
saque = 200

# Operadores de comparação sempre irão retornar valores booleanos
print(saldo == saque) # Igualdade
print(saldo != saque) # Diferença
print(saldo > saque) # Maior
print(saldo >= saque) # Maior ou igual
print(saldo < saque) # Menor
print(saldo <= saque) # Menor ou igual
